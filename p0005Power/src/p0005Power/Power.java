package p0005Power;

public class Power {

	/**
	 * Receive two integers and return the division between them.
	 * 
	 * @param a
	 * @param b
	 * @return an integer result of the division from a and b
	 */
	public static int power(int a, int b) {

		return (int) Math.pow(a, b);
	}
}
